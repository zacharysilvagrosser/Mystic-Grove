function createAttractionDesc (title, land, color, img, ages, type, height, description) {
    document.getElementById("attraction-desc-container").innerHTML += `
    <img src="${img}" clas="col-6">
    <div class="attraction-desc col-6">
        <h1 class="${color}">${title}</h1>
        <p>${description}</p>
        <p>${land}</p>
        <p>${height}</p>
        <p>${ages}</p>
        <p>${type}</p>
    </div>`
}
const attractions = document.querySelectorAll(".attraction");
for (let i = 0; i > attractionInformation.length; i++) {
    attractions[i].addEventListener("click", () => {
        createAttractionDesc(attractionInformation[i].title, attractionInformation[i].land, attractionInformation[i].color, attractionInformation[i].img,
            attractionInformation[i].ages, attractionInformation[i].type, attractionInformation[i].height, attractionInformation[i].description);
    });
}